function renderExamples() {
  if (!examples.length) {
    fullExamples.textContent = "Примеры пока не загружены.";
    return;
  }

  fullExamples.innerHTML = "";
  for (const example of examples) {
    const details = document.createElement("details");
    details.className = "full-example";

    const summary = document.createElement("summary");
    summary.textContent = example.title;

    const pre = document.createElement("pre");
    pre.textContent = example.text;

    details.append(summary, pre);
    fullExamples.appendChild(details);
  }
}

async function copyPrompt(button) {
  await navigator.clipboard.writeText(prompt);
  const originalText = button.textContent;
  button.textContent = "Скопировано";
  setTimeout(() => {
    button.textContent = originalText;
  }, 1200);
}

copyPromptButton.addEventListener("click", () => copyPrompt(copyPromptButton));
copyPromptStepButton.addEventListener("click", () => copyPrompt(copyPromptStepButton));

renderExamples();
